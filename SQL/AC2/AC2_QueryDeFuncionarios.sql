NASCIMENTO DATE NOT NULL,
MONTADORA_PRINCIPAL VARCHAR(100),
SALARIO DECIMAL(20,8),
DATA_CONTRATACAO DATE NOT NULL,
DATA_DEMISSAO DATE
)

